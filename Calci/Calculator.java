package Calci;

public class Calculator {

    public double addition(double a, double b) {
        return a + b;
    }

    public double sub(double a, double b) {
        return a - b;
    }

    public double mul(double a, double b) {
        return a * b;
    }

    public double division(double a, double b) {
        return a / b;
    }
}
